  entry: {
    './js/app.js': ['./js/app.js']
  },
  output: {
    filename: 'app.js.eex',
    path: path.resolve(__dirname, '../lib/oban_web/templates/layout')
  },
  module: {
    rules: [
      {
        test: /\.js$/,
        exclude: /node_modules/,
        use: {
          loader: 'babel-loader'
        }
      },
      {
        test: /\.css$/,
        use: [
          MiniCssExtractPlugin.loader,
          { loader: 'css-loader', options: { importLoaders: 1 } },
          'postcss-loader'
        ]
      }
    ]
  },
  plugins: [
    new MiniCssExtractPlugin({ filename: './app.css.eex' }),
  ]
